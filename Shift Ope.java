public class Main {
	public static void main(String[] args) {
		int number1=5,number2=-4;
		System.out.println("Left Shift:"+(number1<<2));
		System.out.println("Right Shift:"+(number1>>2));
		System.out.println("Unsigned Right Shift:"+(number1>>>2));
		System.out.println("Right Shift:"+(number2>>3));
		System.out.println("Unsigned Right Shift:"+(number1>>>3));
    }
}
